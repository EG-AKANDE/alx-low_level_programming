#include "3-calc.h"

/**
 * op_add - returns sum a plus b
 * @a: first int
 * @b: second int
 *
 * Return: sum a plus b
 */

int op_add(int a, int b)

{
	return (a + b);
}

/**
 * op_sub - returns diff a and b
 * @a: first int
 * @b: second int
 *
 * Return: diff a and b
 */

int op_sub(int a, int b)

{
	return (a - b);
}

/**
 * op_mul - returns product a by b
 * @a: first int
 * @b: second int
 *
 * Return: product a by b
 */

int op_mul(int a, int b)

{
	return (a * b);
}

/**
 * op_div - result of div a by b
 * @a: first int
 * @b: second int
 *
 * Return: result of a by b
 */

int op_div(int a, int b)

{
	return (a / b);
}

/**
 * op_mod - returns remainder of div a / b
 * @a: first int
 * @b: second int
 *
 * Return: remainder of div a / b
 */

int op_mod(int a, int b)

{
	return (a % b);
}
